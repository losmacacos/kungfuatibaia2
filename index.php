                <h3>Kung Fu - Estilo Garra de Águia</h3>
            </div>

            <div class="col-six tab-full left">
                <div class="timeline">

                    <div class="timeline__block">
                        <div class="timeline__bullet"></div>
                        <div class="timeline__header">
                            <h3>Origem</h3>
                            <h5>(Ien Jiao Fan Tzi)</h5>
                        </div>
                        <div class="timeline__desc">
                            <p>A história de nosso estilo começa com um órfão de pai, criado pela mãe viúva. Seu nome era Ó Fei.
                            Quando pequeno, Ó Fei tinha um padrinho e professor chamado Chow, com o qual estudava caligrafia, literatura, matemática, desenho, enfim, fazia seus estudos em geral. Este professor (Chow), foi aluno dos monges do Templo de Shaolin e, além de outras coisas, aprendeu com eles diversas técnicas diferentes de Wushu e, dentre elas, os movimentos da Águia.</p>
                            <p>Naquela época, na China, não havia escolas primárias. As crianças aprendiam as primeiras coisas básicas com os pais. Depois era contratado um professor particular, que lhes ensinaria todas as matérias em geral. Ó Fei começou seus estudos com a mãe e terminou-os com Chow. Além das diversas disciplinas que aprendeu, Chow ensinou-lhe também as mesmas técnicas da Águia que havia aprendido com os monges de Shaolin. Por este motivo podemos afirmar que o estilo de Garra de Águia veio de, embora indiretamente, do Templo de Shaolin. Dizemos indiretamente por que quem criou e fundou oficialmente o estilo que hoje praticamos foi Ó Fei.</p>
                            <p>Podemos dizer, então, que Ó Fei ‘aperfeiçoou’ o estilo Garra de Águia.</p>
                        </div>
                    </div> <!-- end timeline__block -->

                    <div class="timeline__block">
                        <div class="timeline__bullet"></div>
                        <div class="timeline__header">
                            <h3>Origem da palavra KUNG FU</h3>
                            
                        </div>
                        <div class="timeline__desc">    
                            <p>“Kung Fu” é uma antiga expressão que, no dialeto Cantonês, significava "trabalho intensivo" ou "jornada de trabalho", dependendo da forma que era encontrado nos textos antigos. A palavra Kung Fu expandiu-se pelo mundo através do povo Cantão, que tinha fácil acesso ao mar, graças à sua posição geográfica, difundindo sua arte na Europa, África, Oceania e América.</p>
                        </div>
                    </div>

                    <div class="timeline__block">
                        <div class="timeline__bullet"></div>
                        <div class="timeline__header">
                            <h3>Liga Garra de Águia</h3>
                            <h5>Criação</h5>
                        </div>
                        <div class="timeline__desc">
                            <p>A criação da Liga Nacional Garra de Águia de Kung fu surgiu da necessidade de fazer um intercâmbio de treinamentos e de unir o grupo em eventos em todo o Brasil. Os mestres, professores e alunos que treinam o Estilo Garra de Águia no Brasil têm um maior suporte em treino e eventos sob a coordenação do Grão mestre Edilson Moraes, hoje em dia.
                            A Liga Garra de Águia é uma entidade que conta com cerca de mais de 200 professores e instrutores ministrando aulas em várias regiões do Brasil e, seu objetivo é formar e instruir seus filiados e desenvolver o estilo no Brasil.</p>
                        </div>
                    </div> <!-- end timeline__block -->

                </div>
            </div>

            <div class="col-six tab-full right">
                <div class="timeline">

                    <div class="timeline__block">
                        <div class="timeline__bullet"></div>
                        <div class="timeline__header">
                            <h3>Grão Mestre Edilson Moraes</h3>
                            <h5>Liga Garra de Águia</h5>
                        </div>
                        <div class="timeline__desc">
                            <p>Nascido em 1966, na cidade de São Paulo – Brasil, iniciou sua prática no Kung fu aos 12 anos em Setembro de 1978, na União Garra de Águia do Brasil, onde se formou Mestre do estilo.
                            Durante o Campeonato Mundial de Kung fu Brasil 2003, foi elevado à categoria de Mestre 6º Dueen pelo Grão Mestre Li Wing Kay.
                            Com a fundação da Associação Lapa há 22 anos, e ainda a pleno vapor, em 1996 assume a academia Matriz (conhecida também como Cidade Ademar).
                            Em 2010 completou 32 anos de treinamentos ininterruptos, com muito esforço e dedicação.
                            Hoje é responsável por mais de 200 profissionais professores e instrutores, em oito Estados Brasileiros, na maioria destes são Capitais e cidades litorâneas, também com representantes em outros Países com o mais presente na Argentina.
                            É presidente da Federação Paulista de Kuoshu Kung fu Tradicional – FPKT. Fundou a Liga Nacional Garra de Águia de Kung Fu, e a Confederação Brasileira de Artes Marciais Chinesas – C.B.A.M.C., com o intuito de divulgar e desenvolver o Kung fu e dar o devido valor aos Mestres, Professores e Instrutores os quais todos merecem.</p>
                        </div>
                    </div> <!-- end timeline__block -->

                    <div class="timeline__block">
                        <div class="timeline__bullet"></div>
                        <div class="timeline__header">
                            <h3>A FPKT – Federação Paulista de Kuoshu Kung fu Tradicional</h3>
                        </div>
                        <div class="timeline__desc">
                            <p>Constitui-se numa entidade estadual de controle e administração do Kung fu, fundada pelos princiapias mestres de Kung fu no Brasil, tendo como objetivo o crescimento e desenvolvimento do Kung fu em todas as suas modalidades em todo o Estado de São Paulo.

                            Foi fundada em 24/02/1994, e se consolida com a quantidade de eventos e sua organização. Está filiada à Confederação Brasileira de Artes Marciais Chinesas. Hoje a FPKT reúne em seu quadro de associações as grandes lideranças e os maiores expoentes do Kung fu do Estado de São Paulo.</p>
                        </div>
                    </div> <!-- end timeline__block -->

                    <div class="timeline__block">
                        <div class="timeline__bullet"></div>
                        <div class="timeline__header">
                            <h3>A CBAMC – Confederação Brasileira de Artes Marciais Chinesas</h3>
                        </div>
                        <div class="timeline__desc">
                            <p>Assim como a Liga Nacional Garra de Águia e a Federação Paulista de Kung fu, a Confederação tem como intuito enobrecer e desenvolver, mostrando a todos o verdadeiro Kung fu em nosso país. Sendo uma entidade de caráter neutro, onde todos os mestres, professores, instrutores e alunos tem seu devido valor, independente do estilo que pratica. Assim como a FPKT e a Liga Nacional, a CBAMC mantém um grande trabalho em conjunto com mestres parceiros da TSKF, Liga Nacional de Kung fu e  mais de 15 federações filiadas, mostrando a todos o verdadeiro espírito do Kung fu, que é a união. </p>
                        </div>
