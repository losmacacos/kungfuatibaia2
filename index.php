        <div class="row about-content">

            <div class="col-six tab-full left">
                <h3>Kung Fu</h3>
                <p>
                O termo KUNG FU é aplicado às artes marciais chinesas há séculos e significa "trabalho duro". Essa descrição se encaixa nos rigores envolvidos no aprendizado e prática das artes marciais chinesas. De um estudante de Kung Fu se espera a prática diligente. Esta deve envolver fé, resistência, e muitas cansativas e, às vezes, dolorosas horas de treinamento. Combinando isso a altos padrões de moral, caráter e disciplina mental, dão ao estudante um caminho muito árduo a seguir. Existem outros termos nas artes marciais chinesas: "ch’uan shu"(primeira arte), "wu shu"(arte marcial), "kuo shu"(arte nacional). Porém, nenhum desses termos conseguiu ser tão popularizado e conhecido como o "Kung Fu". </p>

                <p> A prática do verdadeiro Kung Fu exige que os ensinamentos influenciem no dia-a-dia (modo de vida integral), em cada aspecto da vida do praticante. O Kung Fu une mente, espírito e corpo. Habilita ações harmoniosas entre os elementos da vida de um ser humano.
                A filosofia reside na importância entre a harmonia e a ordem natural das coisas.</p>

                <p>Esta arte requer de seu adepto um esforço extremo de disciplina no que se refere a comportamento físico e mental. Isto é apenas um item genérico de muitos outros em que se pode dissertar a respeito do Kung Fu, que é muito mais do que uma luta.</p>
            </div>

            <div class="col-six tab-full right">
               <!--  <h3>A arte do Kung Fu</h3> -->
                <br/>
                <br/>
                <p>
                A harmonia que deve existir num praticante de Kung Fu também deve ter origem na ‘escola’ de Kung Fu, da escola para o aluno e do aluno para a sociedade. Na ‘escola’ de Kung Fu é ensinado ao aluno o respeito ao próximo, respeito aos instrutores e a sociedade em que vivem. Em todos os estudantes, repousa a responsabilidade no cuidado com o próximo e com a ‘escola’ de Kung Fu e dessa forma agem como uma família. De fato, na tradição chinesa, os membros de uma ‘escola’ são denominados ‘irmãos’ e ‘irmãs’. O ‘mestre’ visto neste contexto, é o ‘pai’ da ‘escola’ e recebe mais respeito do que um professor. </p>

                <p>O ‘jovem-mestre’ de uma ‘escola’ de Kung Fu é conhecido pelos estudantes como "si-fu". O "si-fu" é uma pessoa altamente versátil que possui além da defesa pessoal, conhecimento filosofia, cultura chinesa e etc. O si-fu não é somente um professor de artes marciais, é também responsável em guiar e agir como o exemplo para os estudantes. Uma frase muito comum no Kung Fu é que "o estudante começa numa sala escura enquanto o mestre está sob a luz do sol". Esta frase demonstra quão importante é o si-fu no desenvolvimento de não somente as habilidades dos estudantes, mas também a atitude e a filosofia.</p>

